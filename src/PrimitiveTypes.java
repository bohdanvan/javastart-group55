public class PrimitiveTypes {

    public static void main(String[] args) {
        // Integer types
        byte b = 100;
        short s = 1000;
        int i = 1000000000;
        long l = 1000000000000000000L;

        // Floating point type
        float f = 1.25f;
        double d = 1.122311241241;

        // Character
        char c = 'a';

        // Logical
        boolean bool = true;

        // String (class, not primitive type)
        String str = "Hello";
    }
}
